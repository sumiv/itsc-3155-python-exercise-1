number_grade = int(input('Enter a grade from 0 to 100: '))

if number_grade >= 90:
    print('You got an A')
elif number_grade >= 80:
    print('You got a B')
elif number_grade >= 70:
    print('You got a C')
elif number_grade >= 60:
    print('You got a D')
else:
    print('You got an F')